class MainClass
{
	public static void main(String arg[])
	{
		int lines=9;
		int star=0;
		for(int i=1;i<=lines;i++)
		{
			for(int j=1;j<=star;j++)
			{
				System.out.print("*");
			}
			System.out.println();
			star++;
		}
	}
}



